     private void AtualizaTable() {
    Connection con = Conexao.AbrirConexao();
    ComprasDAO bd = new ComprasDAO(con);
    List<Compras> lista = new ArrayList<>();
    lista = bd.ListarCompras();
    DefaultTableModel tbm = (DefaultTableModel) jTable1.getModel();
    
     DecimalFormat df = null;
    df = new DecimalFormat("0.00");
    
    while (tbm.getRowCount() > 0) {            
            tbm.removeRow(0);
        }
        int i = 0;
        for (Compras tab : lista) {
            tbm.addRow(new String[i]);
            jTable1.setValueAt(tab.getCodigo(), i, 0);
            jTable1.setValueAt(tab.getProduto(), i, 1); 
            jTable1.setValueAt(tab.getCliente(), i, 2);                     
            jTable1.setValueAt(tab.getQuantidade(), i, 3);
            jTable1.setValueAt(tab.getPreco(), i, 4);
            double total =  tab.getPreco()*tab.getQuantidade();
            jTable1.setValueAt(df.format(total), i, 5);
            i++;
        }
        Conexao.FecharConexao(con);    
}

