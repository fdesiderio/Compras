    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed

        String nome = jTF_Nome.getText();
        String fone = jTF_Telefone.getText();
        String cpf = tfCPF.getText();
        String rua = jTF_Rua.getText();

        if (nome.equals("") || fone.equals("") || cpf.equals("") || rua.equals("")) {

            JOptionPane.showMessageDialog(null, "nenhum campo pode estar vazio",
                "Mercadinho", JOptionPane.WARNING_MESSAGE);

        } else {

            Connection con = Conexao.AbrirConexao();
            ClienteDAO sql = new ClienteDAO(con);
            Cliente a = new Cliente();

            a.setNome(nome);
            a.setTelefone(fone);
            a.setCpf(cpf);
            a.setRua(rua);

            sql.Inserir_Cliente(a);
            Conexao.FecharConexao(con);

            jTF_Nome.setText("");
            jTF_Telefone.setText("");
            tfCPF.setText("");
            jTF_Rua.setText("");

            JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso", "Mercadinho", JOptionPane.INFORMATION_MESSAGE);
            new ConsultarCliente().setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_btSalvarActionPerformed

