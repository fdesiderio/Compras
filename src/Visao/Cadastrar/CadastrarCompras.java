    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        String cliente = jCB_Nome.getSelectedItem().toString();
        String produto = jCB_Nome1.getSelectedItem().toString();
        String quantidade = jTextField1.getText();
        String preco = jTextField2.getText();

        System.out.println(""+cliente);
        System.out.println(""+produto);
        System.out.println(""+quantidade);
        System.out.println(""+preco);
        
        
        if (cliente.equals("") || produto.equals("") || quantidade.equals("") || preco.equals("")) {

            JOptionPane.showMessageDialog(null, "nenhum campo pode estar vazio",
                "Mercadinho", JOptionPane.WARNING_MESSAGE);

        } else {

            Connection con = Conexao.AbrirConexao();
            ComprasDAO sql = new ComprasDAO(con);
            int quant = Integer.parseInt(quantidade);
            double prec = Double.parseDouble(preco);
            Compras a = new Compras();

            a.setCliente(cliente);
            a.setProduto(produto);
            a.setQuantidade(quant);
            a.setPreco(prec);
            
            sql.Inserir_Compras(a);
            
            jCB_Nome.getSelectedItem().toString();
            jCB_Nome1.getSelectedItem().toString();
            jTextField1.setText("");
            jTextField2.setText("");

            JOptionPane.showMessageDialog(null, "Cadastro com sucesso!", "Mercadinho", JOptionPane.INFORMATION_MESSAGE);
            dispose();
            Conexao.FecharConexao(con);
            new Menu().setVisible(true);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

